my_list = [1, 2, 3, 4, 5]
print(my_list)

# Accessing elements
print(my_list[0])
print(my_list[-1])

# Slicing
print(my_list[1:4])

# Modifying elements
my_list[0] = 10
print(my_list)

# Adding elements
my_list.append(6)
print(my_list)

# Removing elements
my_list.remove(3)
print(my_list)

# Length of the list
print(len(my_list))
